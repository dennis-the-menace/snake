package ch.games.dennisrittmeyer.snake;

public class SnakeRectangle {
    private int x;
    private int y;

    public SnakeRectangle(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }


    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }
}
